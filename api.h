/**
 * Adds two integers.
 *
 * @param a an integer.
 * @param b an integer.
 * @returns a + b
 * @since 0.1.0
 */
int add(int a, int b);

/**
 * Multiplies two integers.
 *
 * @param a an integer.
 * @param b an integer.
 * @returns a * b
 * @since 0.1.0
 */
int mult(int a, int b);

/**
 * Returns a bigger number.
 *
 * @param a an integer.
 * @param b an integer.
 * @returns max(a, b)
 * @since @next_version@
 */
int max(int a, int b);
